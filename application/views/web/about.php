                <h4>O que é Afyadata Moçambique?</h4>
                <p class="text-justify">
                    Afyadata Moçambique é um conjunto de ferramentas digitais que servem para colecta de dados, envio de
                    <i>feedback</i> de forma inteligente e retroalimentação da informação para responder eventos de saúde
                    pública.</p>

                AfyaData possui soluções para melhorar a detecção precoce, produção e emissão de relatórios
                oportunos e estabelecimento de resposta rápida, por meio do aplicativo móvel Afyadata na plaraforma Android.
                </p>

                <h4>Como funciona o Painel de Gestão?</h4>
                <p class="text-justify">
                    O Painel de Gestão dos dados de Afyadata Moçambique funciona em conjunto com o aplicativo móvel Android,
                    que é usado para colecta de dados, e posteriormente, o seu envio para processamento.
                </p>
                <p class="text-justify">Quando os dados são enviados ao servidor, são analisados imediatamente usando
                    inteligência natural construída para detectar quaisquer indicadores de padrão anormal, se encontrada
                    anomalia, um alerta é enviado imediatamente para os respectivos gestores/ equipe de resposta rápida,
                    estes por sua vez enviam <i>feedback</i> para o inquiridor.
                </p>

                <h4>Aplicativo Móvel AfyaData Moçambique</h4>
                <p class="text-justify">Este aplicativo é de código aberto, usado para colecta de dados em modo <i>online</i> e
                    <i>offline</i>, Interação entre Inquiridor e Gestor de dados, Alertas de actividades e também contém
                    um Repositório de informação sobre saúde.</p>
                <p class="text-justify">Esta é uma versão personalizada do <a href="https://opendatakit.org/" target="_blank">
                        ODK Collect</a>, que possui os melhores módulos de gestão de formulários. Este aplicativo
                    funciona em conjunto com o Painel de Gestão, que recebe, armazena e pré-analisa dos dados para tomada
                    de decisão.</p>

                <h4>Como funciona AfyaData Moçambique Móvel?</>
                <p class="text-justify">O aplicativo móvel serve para que os inquiridores no campo, colectem e enviem os
                    dados através da Internet, bem como receber <i>feedback</i> dos dados enviados.</p>
                <p class="text-justify">Durante a colecta de dados, o dispositivo Android (telefone ou tablet) pode ficar
                    <i>offline</i>, sem afectar o processo e também tem a capacidade de incluir a localização (GPS), fotos,
                    áudios, textos, números, entre outros tipos de dados.
                </p>


