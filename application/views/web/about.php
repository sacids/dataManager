            <h4 class="text-base leading-8 text-gray-800 mb-2 mt-4">Qu'est-ce qu'Afyadata ?</h4>
            <p class="text-justify text-center text-sm text-gray-600">Afyadata est un outil qui analyse toutes les données collectées sur le terrain et envoie intelligemment des retours au collecteur de données, ainsi qu'une alerte aux responsables de haut niveau si un modèle anormal est découvert dans les données collectées. Cet outil fournit une interface utilisateur graphique aux parties prenantes de la santé pour analyser et visualiser les données collectées via l'application mobile Afyadata pour Android.</p>

            <h4 class="text-base leading-8 text-gray-800 mb-2 mt-4">Comment fonctionne AfyaData Manager ?</h4>
            <p class="text-justify text-center text-sm text-gray-600">
                Cette application fonctionne en collaboration avec l'application Android AfyaData, où vous utilisez
                l'application mobile pour collecter des données sur le terrain, puis vous les envoyez au serveur
                (application AfyaData Manager) pour un traitement ultérieur.</p>
            <p class="text-justify text-center text-sm  text-gray-600">Lorsque les données arrivent sur le serveur, elles sont immédiatement analysées à l'aide de l'intelligence artificielle intégrée pour détecter tout indicateur de modèle anormal. Si un modèle anormal est détecté, une alerte est immédiatement envoyée aux experts de la santé / à l'équipe d'intervention, et des conseils sont envoyés au collecteur de données pendant que d'autres mesures sont prises.
            </p>

            <h4 class="text-base leading-8 text-gray-800 mb-2 mt-4">AfyData Mobile</h4>
            <p class="text-justify text-center text-sm text-gray-600">C'est un outil open source pour collecter et soumettre des données depuis les établissements de santé vers le serveur principal et recevoir des commentaires du serveur principal.</p>
            <p class="text-justify text-center text-sm text-gray-600">Cette version personnalisée d'ODK Collect comprend les meilleurs modules de gestion de formulaires.
                Cette application fonctionne en collaboration avec le côté serveur qui accepte les données, les stocke dans la base de données et analyse la base de données pour prendre des décisions à partir des données soumises.</p>

            <h4 class="text-base leading-8 text-gray-800 mb-2 mt-4">Comment fonctionne AfyaData Mobile ?</h4>
            <p class="text-justify text-center text-sm text-gray-600">L'application fonctionne en tant que client, où les utilisateurs sur le terrain peuvent collecter et soumettre
                des données au serveur central via une connexion Internet, tout en recevant des commentaires sur les données soumises en provenance du serveur / des experts.</p>
            <p class="text-justify text-center text-sm text-gray-600">Lors de la collecte de données, le téléphone peut être hors ligne sans affecter le processus, et il a la capacité d'inclure la localisation GPS et une photo aux données collectées.
